package model;

public class PyCard {
    String suitNnumber;
    int x;
    int y;
    public PyCard() {
        this.suitNnumber = suitNnumber;
        this.x = x;
        this.y = y;
    }

    public String getSuitNnumber() {
        return suitNnumber;
    }

    public void setSuitNnumber(String suitNnumber) {
        this.suitNnumber = suitNnumber;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
